pizza = input ("What pizza would you like? Margherita, Pepperoni or Veggie?")
option1 = input ("Would you like a side order? Yes or no?")
if option1 == "yes":
    side_order = input ("Would you like garlic bread, wings or wedges?")
else:
    side_order = "no side order selected"
    print ("OK, thank you.")
option2 = input ("Would you like a drink? Yes or no?")
if option2 == "yes":
    drink = input ("Would you like water, lemonade, cola or orange juice?")
else:
    drink = "no drink selected"
    print ("Ok, thank you.")

print ("Your order is " + pizza + ", " + side_order + " and " + drink + ".")